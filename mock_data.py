def get_case_names() -> list:
    """获取所有模拟病历名称列表（供下拉框使用）"""
    return [c["name"] for c in MOCK_CASES]


def get_case_by_index(index: int) -> dict:
    """根据索引获取模拟病历（0-based）"""
    if 0 <= index < len(MOCK_CASES):
        return MOCK_CASES[index]
    return MOCK_CASES[0]


def get_case_by_name(name: str) -> dict:
    """根据名称获取模拟病历"""
    for c in MOCK_CASES:
        if c["name"] == name:
            return c
    return MOCK_CASES[0]


def fill_case(name: str):
    """根据下拉框选择填充所有输入字段，返回5个值"""
    case = get_case_by_name(name)
    return (
        case["records"],
        case["family_history"],
        case["excluded_diseases"],
        case["persistent_symptoms"],
        case["abnormal_labs_summary"],
    )
